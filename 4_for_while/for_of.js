// iterar cada letra em uma palavra
function logLetras(palavra){
    for(letra of palavra){
        console.log(letra);
    }
}
const palavra = "abacaxi";

logLetras(palavra)

function logNumeros(num){
    for(num of nums){
        console.log(num);
    }
}
const nums = [30,20,233,2];

logNumeros(nums)