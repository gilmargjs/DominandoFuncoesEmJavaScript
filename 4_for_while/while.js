// while
function exemploWhile(){
    let num = 0;
    while(num <= 5){
        console.log(num);
        num++
    }
}
exemploWhile()

// do while

function exemploDoWhile(){
    let num = 0;
    do{
        console.log(num);
        num++;
    }while(num <= 5)
}

exemploDoWhile()

